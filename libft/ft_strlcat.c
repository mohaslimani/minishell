#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	k;

	j = 0;
	k = ft_strlen(dst);
	i = k;
	if (ft_strlen(dst) >= size)
		return (ft_strlen(src) + size);
	while ((i < size - 1) && src[j])
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (ft_strlen(src) + k);
}
